public class Ejercicio_Uno {
    public int ak(int a1, int b1,int c1){
        return (a1+(b1*c1));
    }
    public int k(int a1, int b1,int c1){
        return ((a1+b1)*c1);
    }
    public float b(float a1, float b1,float c1){
        return ((a1/b1)+c1);
    }
    public float c(float a1, float b1, float c1, float d1){
        return ((a1/b1)+(c1/d1));
    }
    public float def (float a1, float b1){
        return a1/b1;
    }
    public float ghim(float a1, float b1){
        return a1%b1;
    } 
    public float j(float a1, float b1, float c1){
        return ((a1%b1)+c1);
    }
    public float l(float a1, float b1, float c1){
        return (a1/(b1+c1));
    }
}
